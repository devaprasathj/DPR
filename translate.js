/**
 * Sets the language of the page content.
 * @param {string} lang - The language code (e.g., 'en', 'hi').
 */
function setLanguage(lang) {
    // Save the selected language to local storage for persistence
    localStorage.setItem('dprLanguage', lang);

    document.querySelectorAll('[data-translate-key]').forEach(element => {
        const key = element.getAttribute('data-translate-key');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
}

/**
 * Initializes the language switcher functionality.
 */
function initLanguageSwitcher() {
    const languageSelector = document.getElementById('languageSelector');
    
    // Check for a saved language in local storage
    const savedLang = localStorage.getItem('dprLanguage') || 'en';

    // Set the dropdown to the saved language
    if (languageSelector) {
        languageSelector.value = savedLang;
    }
    
    // Apply the saved or default language
    setLanguage(savedLang);

    // Add event listener to change language when the user selects a new one
    if (languageSelector) {
        languageSelector.addEventListener('change', (event) => {
            setLanguage(event.target.value);
        });
    }
}

